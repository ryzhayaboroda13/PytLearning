    "–ò–¥–µ–∞–ª—å–Ω–æ! –î–≤–∏–∂–µ–º—Å—è –¥–∞–ª—å—à–µ!",
    "–ü—Ä–µ–≤–æ—Å—Ö–æ–¥–Ω–æ! –°–ª–µ–¥—É—é—â–∞—è —Ç–µ–º–∞ –∂–¥–µ—Ç!",
    "–¢—ã —É–≤–µ—Ä–µ–Ω–Ω–æ –∏–¥–µ—à—å –∫ —Å–≤–æ–µ–π —Ü–µ–ª–∏!",
