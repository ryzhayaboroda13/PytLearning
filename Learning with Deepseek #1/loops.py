#–º–æ–π —Å–ø–∏—Å–æ–∫ –ø—Ä–æ–µ–∫—Ç–æ–≤
my_projects = ["–¢–µ–ª–µ–≥—Ä–∞–º-–±–æ—Ç", "–ò–≥—Ä–∞ –Ω–∞ Android", "–í–µ–±-—Å–∞–π—Ç", "–ù–µ–π—Ä–æ—Å–µ—Ç—å"]

# 1. –¶–∏–∫–ª FOR - –∏–¥–µ–∞–ª–µ–Ω –¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ —Å–ø–∏—Å–∫–µ
print("=> –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã —Å–ø–∏—Å–∫–∞ –Ω–∞–ø—Ä—è–º—É—é")     # –≤—ã–≤–æ–¥–∏–º –∏—Ö –≤—Å–µ —Å –ø–æ–º–æ—â—å—é —Ü–∏–∫–ª–∞
for project in my_projects:         # –î–ª—è –ö–ê–ñ–î–û–ì–û project –í my_projects
    print("–ü—Ä–æ–µ–∫—Ç : ", project)
print("-----")

# 2. –¶–∏–∫–ª FOR —Å —Ñ—É–Ω–∫—Ü–∏–µ–π enumerate() - –ø–æ–ª—É—á–∞–µ–º –∏ —ç–ª–µ–º–µ–Ω—Ç, –∏ –µ–≥–æ –Ω–æ–º–µ—Ä (–∏–Ω–¥–µ–∫—Å)
print("=> –í–∞—Ä–∏–∞–Ω—Ç 2: –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –∏ –∏—Ö –Ω–æ–º–µ—Ä–∞")

# enumerate –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–∞—Ä—ã (index, element). start=1 –Ω–∞—á–∏–Ω–∞–µ—Ç —Å—á–µ—Ç —Å 1, –∞ –Ω–µ —Å 0.
for number, project in enumerate(my_projects, start=1):
    print(f"{number}-—ã–π –ø—Ä–æ–µ–∫—Ç: {project}")
    # f-—Å—Ç—Ä–æ–∫–∞ –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
print("-----")

# 3. –¶–∏–∫–ª WHILE - –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è, –ø–æ–∫–∞ —É—Å–ª–æ–≤–∏–µ –ò–°–¢–ò–ù–ù–û (True)
print("=> –í–∞—Ä–∏–∞–Ω—Ç 3: –û–±—Ä–∞—Ç–Ω—ã–π –æ—Ç—Å—á–µ—Ç —Å –ø–æ–º–æ—â—å—é while")
countdown = 3       # –°–æ–∑–¥–∞–µ–º —Å—á–µ—Ç—á–∏–∫
while countdown > 0:        # –ü–æ–∫–∞ countdown –ë–û–õ–¨–®–ï 0
    print(f"–î–æ —Å—Ç–∞—Ä—Ç–∞: {countdown}...")
    countdown = countdown - 1       # –£–º–µ–Ω—å—à–∞–µ–º —Å—á–µ—Ç—á–∏–∫ –Ω–∞ 1 
    #(–º–æ–∂–Ω–æ –Ω–∞–ø–∏—Å–∞—Ç—å countdown -= 1) 
print("–ü–æ–µ—Ö–∞–ª–∏! üöÄ")
print("---")

# 4. –ë–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª –∏ –≤—ã—Ö–æ–¥ –∏–∑ –Ω–µ–≥–æ (–∫–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ break)
print("=> –í–∞—Ä–∏–∞–Ω—Ç 4: –ë–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª –¥–ª—è –¥–∏–∞–ª–æ–≥–∞ (–¥–ª—è –≤—ã—Ö–æ–¥–∞ –Ω–∞–ø–∏—à–∏ '—Å—Ç–æ–ø')")
while True:     # –¶–∏–∫–ª –±—É–¥–µ—Ç –∫—Ä—É—Ç–∏—Ç—å—Å—è –≤–µ—á–Ω–æ...
    user_input = input("–¢—ã –≥–æ—Ç–æ–≤ –ø—Ä–æ–¥–æ–ª–∂–∞—Ç—å? (–Ω–∞–ø–∏—à–∏ '—Å—Ç–æ–ø' –¥–ª—è –≤—ã—Ö–æ–¥–∞): ")
    if user_input.lower() == "—Å—Ç–æ–ø":
        print("–î–∏–∞–ª–æ–≥ –∑–∞–≤–µ—Ä—à–µ–Ω. –î–æ —Å–ª–µ–¥—É—é—â–µ–≥–æ —Ä–∞–∑–∞!")
        break  # –ö–æ–º–∞–Ω–¥–∞ break –ù–ï–ú–ï–î–õ–ï–ù–ù–û –≤—ã—Ö–æ–¥–∏—Ç –∏–∑ —Ü–∏–∫–ª–∞
    else:
        print(f"–û—Ç–ª–∏—á–Ω–æ! –¢—ã —Å–∫–∞–∑–∞–ª: '{user_input}'. –ü—Ä–æ–¥–æ–ª–∂–∞–µ–º –≤ —Ç–æ–º –∂–µ –¥—É—Ö–µ!")