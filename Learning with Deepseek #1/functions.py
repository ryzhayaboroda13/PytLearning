# Импортируем модуль random для генерации случайных чисел
import random

# Объявляем нашу первую функцию с именем 'greet_user'
# Ключевое слово 'def' говорит Python, что мы определяем функцию
# 'user_name' - это параметр, то, что мы передаем в функцию
def greet_user(user_name):
    """
    Эта функция приветствует пользователя по имени.
    Она принимает один параметр: user_name.
    """
    # Тело функции (все что с отступом) - это то, что она делает
    print(f"Привет, {user_name}! Добро пожаловать в мир функций! 🎉")
    print("Сейчас я сгенерирую для тебя случайное число...")

# Объявляем вторую функцию без параметров
def generate_lucky_number():
    """
    Эта функция генерирует и возвращает "счастливое" число от 1 до 100.
    Она не принимает параметров, но возвращает результат.
    """
    lucky_number = random.randint(1, 100)  # Генерируем случайное число
    return lucky_number  # Возвращаем результат работы функции

# Объявляем третью функцию с несколькими параметрами
def check_number(user_number, lucky_number):
    """
    Эта функция сравнивает два числа и дает совет.
    Принимает два параметра: user_number и lucky_number.
    """
    if user_number > lucky_number:
        return "Твое число слишком большое! 📈"
    elif user_number < lucky_number:
        return "Твое число слишком маленькое! 📉"
    else:
        return "Угадал! Ты победил! 🎯"

# === НАЧАЛО ОСНОВНОЙ ПРОГРАММЫ ===
# Этот код будет выполняться при запуске файла

print("=== ДЕМОНСТРАЦИЯ РАБОТЫ ФУНКЦИЙ ===")
print()

# Вызываем функцию greet_user и передаем ей твое имя как аргумент
greet_user("Daniel")  # "Daniel" - это аргумент, который попадает в параметр user_name
print()

# Вызываем функцию generate_lucky_number и сохраняем результат в переменную
secret_number = generate_lucky_number()  # Функция возвращает число, мы его сохраняем
print(f"Твое счастливое число: {secret_number} (но это секрет!)")
print()

# Просим пользователя ввести число
user_guess = int(input("Попробуй угадать число от 1 до 100: "))

# Вызываем функцию check_number, передаем два аргумента и сохраняем результат
result = check_number(user_guess, secret_number)

# Выводим результат сравнения
print(result)
print()

# Демонстрация повторного использования функций
print("=== ПОВТОРНОЕ ИСПОЛЬЗОВАНИЕ ФУНКЦИЙ ===")
greet_user("Разработчик")  # Та же функция, но с другим аргументом
another_number = generate_lucky_number()  # Генерируем еще одно число
print(f"Новое счастливое число: {another_number}")